function openSlideMenu(){
    document.getElementById('side-menu').style.width= '100px';
    document.getElementById('side-menu').style.marginLeft= '400px';
    document.getElementById('side-menu').style.marginTop= '65px';
    document.getElementById('side-menu').style.height='250px';

    console.log("button clicked");
}
function closeSlideMenu(){
    document.getElementById('side-menu').style.width= '200px';
}